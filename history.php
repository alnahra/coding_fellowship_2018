				St. Louis' history of voluntary community segregation is the main reason for the existence of 90 different municipalities in the metropolitan area in the first place.
				The practice of legally dividing communities to avoid African American integration became so commonplace that it is now simply accepted
				as fact that these municipalities exist distinct from each other. There is no reason for the area's fragmentation aside from this history of racism.
				Allowing such small areas function as their own governments means that the tax bases for each area are limited in scope, and many of the public services remaining do not make enough moeny to continue to function.
				These underfunded departments rely on tickets and other incedental sources of revenue to bankroll police forces, firefighters, and myriad other services. Even when they're unnecessary.
